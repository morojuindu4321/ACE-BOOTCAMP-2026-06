import numpy as np
arr = np.array([1,4,5,8,9])
print("Multiplication:" ,arr*2)#Multiplication
print("Addition:" ,arr+5)#Addition
print("Subtraction:" ,arr-3)#Subtraction
print("Division:" ,arr/3)#Division
print("Double Division:" ,arr//3)#Double Division
print("Modulus:" ,arr%3)#Modulus
print("Power Square:" ,arr**2)#Power Square
print("Power Cube:" ,arr**3)#Power Cube
print(np.add(2,arr))
print(np.subtract(1,arr))
print(np.multiply(2,arr))
print(np.divide(3,arr))
print(np.power(2,arr))
print(np.power(3,arr))
print(np.mod(2,arr))

